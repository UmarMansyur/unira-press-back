class News {
  setId(id) {
    this.id = id;
    return this;
  }

  setJudulBerita(judul_berita){
    this.judul_berita = judul_berita;
    return this;
  }

  setIsi(isi) {
    this.isi = isi;
    return this;
  }

  setCover(cover){
    this.cover = cover;
    return this;
  }

  setDilihat(dilihat){
    this.dilihat = dilihat;
    return this;
  }

  setPenulisId(penulis_id){
    this.penulis_id = penulis_id;
    return this;
  }
}